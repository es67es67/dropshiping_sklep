const fs = require('fs');
const path = require('path');

async function countNewSIMC() {
    try {
        console.log('üìä LICZƒò WIERSZE W NOWYM PLIKU SIMC...');
        
        const SIMC_FILE = path.join(__dirname, '../inne/dane adresowe gus/SIMC_Adresowy_2025-07-07.csv');
        
        if (!fs.existsSync(SIMC_FILE)) {
            console.log('‚ùå Plik SIMC nie istnieje:', SIMC_FILE);
            return;
        }
        
        console.log('üìÅ Plik SIMC:', SIMC_FILE);
        
        // Wczytaj plik
        const fileContent = fs.readFileSync(SIMC_FILE, 'utf8');
        const lines = fileContent.split('\n');
        
        // Usu≈Ñ puste linie
        const nonEmptyLines = lines.filter(line => line.trim() !== '');
        
        // Pierwsza linia to nag≈Ç√≥wek
        const headerLine = nonEmptyLines[0];
        const dataLines = nonEmptyLines.slice(1);
        
        console.log('\nüìä WYNIKI:');
        console.log('üìã Wszystkie linie:', lines.length);
        console.log('üìã Niepuste linie:', nonEmptyLines.length);
        console.log('üìã Linia nag≈Ç√≥wka:', headerLine ? 'TAK' : 'NIE');
        console.log('üìã Linie z danymi:', dataLines.length);
        
        // Sprawd≈∫ nag≈Ç√≥wek
        console.log('\nüìã NAG≈Å√ìWEK:');
        console.log(headerLine);
        
        // Sprawd≈∫ pierwsze kilka wierszy
        console.log('\nüîç PIERWSZE 5 WIERSZY:');
        dataLines.slice(0, 5).forEach((line, index) => {
            const columns = line.split(';');
            console.log(`${index + 1}. ${columns.join(' | ')}`);
        });
        
        // Sprawd≈∫ ostatnie kilka wierszy
        console.log('\nüîç OSTATNIE 5 WIERSZY:');
        dataLines.slice(-5).forEach((line, index) => {
            const columns = line.split(';');
            console.log(`${dataLines.length - 4 + index}. ${columns.join(' | ')}`);
        });
        
        // Sprawd≈∫ rozk≈Çad RM (rodzaj miejscowo≈õci)
        console.log('\nüìä ROZK≈ÅAD RODZAJ√ìW MIEJSCOWO≈öCI (RM):');
        const rmCounts = {};
        dataLines.forEach(line => {
            const columns = line.split(';');
            if (columns.length > 4) {
                const rm = columns[4]; // RM to 5-ta kolumna (indeks 4)
                rmCounts[rm] = (rmCounts[rm] || 0) + 1;
            }
        });
        
        Object.entries(rmCounts).sort((a, b) => b[1] - a[1]).forEach(([rm, count]) => {
            let description = '';
            switch(rm) {
                case '01': description = ' (wie≈õ)'; break;
                case '96': description = ' (miasto)'; break;
                case '00': description = ' (czƒô≈õƒá miejscowo≈õci)'; break;
                case '02': description = ' (kolonia)'; break;
                case '03': description = ' (przysi√≥≈Çek)'; break;
                case '04': description = ' (osada)'; break;
                case '05': description = ' (osada le≈õna)'; break;
                case '06': description = ' (osiedle)'; break;
                case '07': description = ' (schronisko turystyczne)'; break;
                case '95': description = ' (dzielnica)'; break;
                case '98': description = ' (delegatura)'; break;
                case '99': description = ' (czƒô≈õƒá miasta)'; break;
            }
            console.log(`RM ${rm}${description}: ${count} wierszy`);
        });
        
        // Policz g≈Ç√≥wne miejscowo≈õci (RM = 01, 96)
        const mainPlaces = (rmCounts['01'] || 0) + (rmCounts['96'] || 0);
        console.log(`\n‚úÖ G≈Å√ìWNE MIEJSCOWO≈öCI (RM 01 + 96): ${mainPlaces}`);
        
        console.log('\n‚úÖ LICZBA WSZYSTKICH WPIS√ìW W CSV:', dataLines.length);
        
    } catch (error) {
        console.error('‚ùå B≈ÇƒÖd:', error.message);
    }
}

countNewSIMC(); 